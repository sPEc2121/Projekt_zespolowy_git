import sqlite3


# Opening (or creating if not exists) database file
conn = sqlite3.connect('msbox_database.db')
cursor = conn.cursor()

# Tabele do zachowania
tables_to_keep = [
    'django_migrations',
    'django_admin_log',
    'django_content_type',
    'auth_permission',
    'auth_group',
    'auth_user', 
    'sqlite_sequence'
]

# Pobranie nazw wszystkich tabel w bazie danych
cursor.execute("SELECT name FROM sqlite_master WHERE type='table';")
all_tables = cursor.fetchall()

# Wyczyszczenie zawartości każdej tabeli, której nazwa nie znajduje się na liście tabel do zachowania
for table in all_tables:
    table_name = table[0]
    if table_name not in tables_to_keep:
        cursor.execute("DROP TABLE {}".format(table_name))

# Creating tables from schema

# Defining tables
create_table_user = '''
CREATE TABLE IF NOT EXISTS USER (
  Id INTEGER PRIMARY KEY AUTOINCREMENT,
  Mail VARCHAR(30) UNIQUE NOT NULL,
  Password VARCHAR(30) NOT NULL,
  Active BOOLEAN NOT NULL
);  
'''

create_table_personal_data = '''
CREATE TABLE IF NOT EXISTS PERSONAL_DATA (
  Id INTEGER PRIMARY KEY AUTOINCREMENT,
  UserId INTEGER NOT NULL,
  FirstName VARCHAR(30) NOT NULL,
  LastName VARCHAR(30) NOT NULL,
  BirthDate DATETIME NOT NULL,
  Sex BOOLEAN,
  DefaultAddress VARCHAR(30) NOT NULL,
  DefaultPostalcode VARCHAR(30) NOT NULL,
  DefaultLocation VARCHAR(30) NOT NULL,
  Phone VARCHAR(30) NOT NULL,
  Country VARCHAR(30) NOT NULL,
  Latitude DECIMAL(4,20) NOT NULL,
  Longitude DECIMAL(4,20) NOT NULL,
  FOREIGN KEY (UserId) REFERENCES User(Id)
);
'''

create_table_machine = '''
CREATE TABLE IF NOT EXISTS MACHINE (
  Id INTEGER PRIMARY KEY AUTOINCREMENT,
  Address VARCHAR(30) NOT NULL,
  PostalCode VARCHAR(30) NOT NULL,
  Location VARCHAR(30) NOT NULL,
  Country VARCHAR(30) NOT NULL,
  IsMobile BOOLEAN NOT NULL,
  Latitude DECIMAL(4,20) NOT NULL,
  Longitude DECIMAL(4,20) NOT NULL
);
'''

create_table_chamber = '''
CREATE TABLE IF NOT EXISTS CHAMBER (
  Id INTEGER PRIMARY KEY AUTOINCREMENT,
  MachineId INTEGER NOT NULL,
  Size VARCHAR(30) NOT NULL,
  Status BOOLEAN NOT NULL,
  FOREIGN KEY (MachineId) REFERENCES Machine(Id)
);
'''

create_table_chamber_details = '''
CREATE TABLE IF NOT EXISTS CHAMBER_DETAILS (
  ChamberId INTEGER NOT NULL,
  Price INTEGER NOT NULL,
  DateFrom DATETIME NOT NULL,
  DateTo DATETIME,
  Active BOOLEAN NOT NULL,
  Width INTEGER NOT NULL,
  Length INTEGER NOT NULL,
  Height INTEGER NOT NULL,
  Weight INTEGER NOT NULL,
  FOREIGN KEY (ChamberId) REFERENCES Chamber(Id)
);
'''

create_table_payment_method = '''
CREATE TABLE IF NOT EXISTS PAYMENT_METHOD (
  Id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
  PaymentName VARCHAR(30) NOT NULL
);
'''

create_table_favourite_machine = '''
CREATE TABLE IF NOT EXISTS FAVOURITE_MACHINES (
  Id INTEGER PRIMARY KEY AUTOINCREMENT,
  UserId INTEGER NOT NULL,
  MachineId INTEGER NOT NULL,
  FOREIGN KEY (UserId) REFERENCES User(Id),
  FOREIGN KEY (MachineId) REFERENCES Machine(Id)
);
'''

create_table_role = '''
CREATE TABLE IF NOT EXISTS ROLE (
  Id INTEGER PRIMARY KEY AUTOINCREMENT,
  RoleName VARCHAR(30) NOT NULL
);
'''

create_table_status = '''
CREATE TABLE IF NOT EXISTS STATUS (
  Id INTEGER PRIMARY KEY AUTOINCREMENT,
  StatusName VARCHAR(30) NOT NULL
);
'''

create_table_user_role = '''
CREATE TABLE IF NOT EXISTS USER_ROLE (
  Id INTEGER PRIMARY KEY AUTOINCREMENT,
  UserId INTEGER NOT NULL,
  RoleId INTEGER NOT NULL,
  FOREIGN KEY (UserId) REFERENCES User(Id),
  FOREIGN KEY (RoleId) REFERENCES Role(Id)
);
'''

create_table_order = '''
CREATE TABLE IF NOT EXISTS ORDER_ (
  Id INTEGER PRIMARY KEY AUTOINCREMENT,
  StatusId INTEGER NOT NULL,
  SenderId INTEGER NOT NULL,
  ReceiverId INTEGER NOT NULL,
  ChamberId INTEGER NOT NULL,
  PaymentMethodId INTEGER NOT NULL,
  Description VARCHAR(30),
  Active BOOLEAN NOT NULL,
  StartDate DATETIME NOT NULL,
  EndDate DATETIME,
  MachineIdFrom INTEGER NOT NULL,
  MachineIdTo INTEGER NOT NULL,
  DeliveryDate DATETIME,
  ReturnDeliveryDate DATETIME,
  Postponed BOOLEAN,
  PostponedDays INTEGER,
  IsReturn BOOLEAN NOT NULL,
  DeliveryCost INTEGER NOT NULL,
  FOREIGN KEY (StatusId) REFERENCES Status(Id),
  FOREIGN KEY (SenderId) REFERENCES User(Id),
  FOREIGN KEY (ReceiverId) REFERENCES User(Id),
  FOREIGN KEY (ChamberId) REFERENCES Chamber(Id),
  FOREIGN KEY (PaymentMethodId) REFERENCES PaymentMethod(Id)
);
'''
create_table_order_chamber ='''
CREATE TABLE IF NOT EXISTS ORDER_CHAMBER (
  OrderId INTEGER NOT NULL,
  ChamberId INTEGER NOT NULL,
  FOREIGN KEY (OrderId) REFERENCES ORDER_(Id),
  FOREIGN KEY (ChamberId) REFERENCES CHAMBER(Id)
);
'''

